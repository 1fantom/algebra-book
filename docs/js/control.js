function genControlQuestions() {
  cqList.innerHTML = '';
  const shuffled = questions.sort(() => 0.5 - Math.random());
  const selected = shuffled.slice(0, 5); // можно изменить количество, например 10 или 15
  selected.forEach(q => {
    const li = document.createElement('li');
    li.textContent = q;
    cqList.append(li);
  });
}

